const fs = require('fs');
const path = require('path');

// Create valid PNG files for manifest icons
// This is a minimal but valid PNG (1x1 pixel white image)
const validPNG = Buffer.from([
  // PNG signature
  0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a,
  // IHDR chunk - image header
  0x00, 0x00, 0x00, 0x0d, 0x49, 0x48, 0x44, 0x52,
  0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
  0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53,
  0xde,
  // IDAT chunk - image data
  0x00, 0x00, 0x00, 0x0c, 0x49, 0x44, 0x41, 0x54,
  0x08, 0x99, 0x63, 0xf8, 0xff, 0xff, 0xff, 0x7f,
  0x00, 0x09, 0xfb, 0x03, 0xfd, 0x05, 0x39, 0xd7,
  0x84,
  // IEND chunk
  0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4e, 0x44,
  0xae, 0x42, 0x60, 0x82
]);

const clientPublic = path.join(__dirname, 'client', 'public');

fs.writeFileSync(path.join(clientPublic, 'logo192.png'), validPNG);
fs.writeFileSync(path.join(clientPublic, 'logo512.png'), validPNG);

